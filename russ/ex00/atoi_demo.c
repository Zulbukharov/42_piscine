#include "prot.h"

int		atoi_demo(char *str)
{
	int		i;
	int		a;
	int		c;
	char	dot;
	char	obst;

	i = 0;
	a = 0;
	c = 0;
	while (str[i] >= 48 && str[i] <= 57)
	{
		a = (a * 10) + (str[i] - '0');
		i++;
	}
	while (str[i++] != '\0')
		c++;
	if (c != 4)
		return (0);
	return (a);
}
