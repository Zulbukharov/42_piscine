int	ft_atoi(char *str)
{
	int n;
	int t;

	n = 0;
	while (*str == '\n' || *str == '\t' || *str == '\v'
			|| *str == ' ' || *str == '\r' || *str == '\f')
		str++;
	if (*str == '+')
		str++;
	else if (*str == '-')
	{
		n = 1;
		str++;
	}
	if (!*str || *str < '0' || *str > '9')
		return (0);
	t = *str - '0';
	while ((*++str && *str >= '0') && *str <= '9')
	{
		t = t * 10 + (*str - 48);
	}
	if (n)
		return (-t);
	else
		return (t);
}
