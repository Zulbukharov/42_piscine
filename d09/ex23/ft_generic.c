#include <unistd.h>

void	ft_generic(void)
{
	int		i;
	char	*str;

	str = "Tut tut ; Tut tut";
	i = 0;
	while (str[i] != '\0')
	{
		write(1, &str[i], 1);
		i++;
	}
	write(1, "\n", 1);
}
