int		ft_str_is_numeric(char *str)
{
	int res;
	int i;

	res = 1;
	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 48 && str[i] <= 57)
			i++;
		else
			return (res);
	}
	return (res);
}
