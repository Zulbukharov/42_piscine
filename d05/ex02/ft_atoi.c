int		ft_atoi(char *str)
{
	int res;
	int neg;

	res = 0;
	neg = 1;
	while (*str != '\0')
	{
		if (*str == ' ' || *str == '\t' || *str == '\n' ||
			*str == '\r' || *str == '\v')
			str++;
		else if (*str - '0' >= 0 && *str - '0' <= 9)
		{
			res = res * 10 + (*str - '0');
			str++;
		}
		else if (*str == '-')
		{
			neg *= -1;
			str++;
		}
		else if (*str == '+' || *str == '-')
			str++;
	}
	return (res * neg);
}
