#include "head.h"

void	ft_putnbr(int a)
{
	if (a < 0)
	{
		ft_putchar('-');
		a = -a;
	}
	if (a > 9)
		ft_putnbr(a / 10);
	ft_putchar(48 + a % 10);
}
