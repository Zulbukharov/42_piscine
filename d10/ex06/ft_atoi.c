int		ft_atoi(char *str)
{
	int i;
	int a;
	int c;

	i = 0;
	a = 0;
	c = 1;
	while (str[i] == ' ')
		i++;
	if (str[i] == '-')
	{
		c = -1;
		i++;
	}
	if (str[i] == '+')
		i++;
	while (str[i] >= 48 && str[i] <= 57)
	{
		a = (a * 10) + (str[i] - '0');
		i++;
	}
	return (a * c);
}
