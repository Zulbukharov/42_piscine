#include <stdlib.h>

char	*rush01(int x, int y)
{
	int		i;
	int		j;
	char	*s;

	s = (char*)malloc(++x * y + 1);
	j = 0;
	while (j++ < y)
	{
		i = 0;
		while (i++ < x - 1)
		{
			if (j == 1 && i == 1)
				s[x * (j - 1) + i - 1] = '/';
			else if ((j == 1 && i == x - 1) || (j == y && i == 1))
				s[x * (j - 1) + i - 1] = '\\';
			else if (j == y && i == x - 1)
				s[x * (j - 1) + i - 1] = '/';
			else if ((j == 1 || j == y) || (i == 1 || i == x - 1))
				s[x * (j - 1) + i - 1] = '*';
			else
				s[x * (j - 1) + i - 1] = ' ';
		}
		if (x - 1 > 0 && y > 0)
			s[x * (j - 1) + i - 1] = '\n';
	}
	s[x * y] = 0;
	return (s);
}
